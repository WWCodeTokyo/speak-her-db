<template>
  <v-navigation-drawer
    :model-value="drawer"
    location="right"
    temporary
    color="primary"
  >
    <v-list>
      <div
        v-for="item in items"
        :key="item.title"
      >
        <v-list-item
          :href="item.path"
          :title="item.title"
          :prepend-icon="item.icon"
          link
        />
      </div>
      <v-list-item>
        <locale-switcher />
      </v-list-item>
    </v-list>
  </v-navigation-drawer>
</template>

<script>
import LocaleSwitcher from './LocaleSwitcher.vue';

export default {
  components: {
    LocaleSwitcher,
  },
  props: {
    show: {
      type: Boolean,
      required: true,
    },
    items: {
      type: Array,
      required: true,
    },
  },
  data: () => ({
    drawer: false,
  }),
  watch: {
    show() {
      this.drawer = this.show;
    },
    drawer() {
      if (!this.drawer) {
        this.$emit('close-drawer');
      }
    },
  },
};
</script>
